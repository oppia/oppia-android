@RunWith(AndroidJUnit4::class)
@LooperMode(LooperMode.Mode.PAUSED)
@Config(application = BottomSheetOptionsMenuTest.TestApplication::class)
class BottomSheetOptionsMenuTest {

  @Inject
  lateinit var testCoroutineDispatchers: TestCoroutineDispatchers

  @Inject
  lateinit var context: Context

  @Before
  fun setUp() {
    Intents.init()
    setUpTestApplicationComponent()
    testCoroutineDispatchers.registerIdlingResource()
  }

  @After
  fun tearDown() {
    Intents.release()
    testCoroutineDispatchers.unregisterIdlingResource()
  }

  @Test
  fun bottomSheetOptionsMenu_openBottomSheet_selectHelpInBottomSheet_opensHelpActivity() {
    ActivityScenario.launch(BottomSheetOptionsMenuTestActivity::class.java).use {
      onView(withId(R.id.action_bottom_sheet_options_menu)).perform(click())
      testCoroutineDispatchers.runCurrent()
      onView(withText(context.getString(R.string.menu_help))).inRoot(isDialog()).perform(click())
      testCoroutineDispatchers.runCurrent()
      intended(
        hasExtra(
          HelpActivity.BOOL_IS_FROM_NAVIGATION_DRAWER_EXTRA_KEY,
          false
        )
      )
    }
  }

  @Test
  fun bottomSheetOptionsMenu_openBottomSheet_selectOptionsInBottomSheet_opensOptionsActivity() {
    ActivityScenario.launch(BottomSheetOptionsMenuTestActivity::class.java).use {
      onView(withId(R.id.action_bottom_sheet_options_menu)).perform(click())
      testCoroutineDispatchers.runCurrent()
      onView(withText(context.getString(R.string.menu_options))).inRoot(isDialog()).perform(click())
      testCoroutineDispatchers.runCurrent()
      intended(
        hasExtra(
          OptionsActivity.BOOL_IS_FROM_NAVIGATION_DRAWER_EXTRA_KEY,
          false
        )
      )
    }
  }

  @Test
  fun bottomSheetOptionsMenu_openBottomSheet_selectCloseInBottomSheet_closesBottomSheet() {
    ActivityScenario.launch(BottomSheetOptionsMenuTestActivity::class.java).use {
      onView(withId(R.id.action_bottom_sheet_options_menu)).perform(click())
      testCoroutineDispatchers.runCurrent()
      onView(withText(context.getString(R.string.bottom_sheet_options_menu_close)))
        .inRoot(isDialog())
        .perform(click())
      testCoroutineDispatchers.runCurrent()
      onView(withId(R.id.options_menu_bottom_sheet_container)).check(doesNotExist())
    }
  }

  private fun setUpTestApplicationComponent() {
    ApplicationProvider.getApplicationContext<TestApplication>().inject(this)
  }

  // TODO(#59): Figure out a way to reuse modules instead of needing to re-declare them.
  @Singleton
  @Component(
    modules = [
      TestDispatcherModule::class, ApplicationModule::class, RobolectricModule::class,
      PlatformParameterModule::class, PlatformParameterSingletonModule::class,
      LoggerModule::class, ContinueModule::class, FractionInputModule::class,
      ItemSelectionInputModule::class, MultipleChoiceInputModule::class,
      NumberWithUnitsRuleModule::class, NumericInputRuleModule::class, TextInputRuleModule::class,
      DragDropSortInputModule::class, InteractionsModule::class, GcsResourceModule::class,
      GlideImageLoaderModule::class, ImageParsingModule::class, HtmlParserEntityTypeModule::class,
      QuestionModule::class, TestLogReportingModule::class, AccessibilityTestModule::class,
      ImageClickInputModule::class, LogStorageModule::class, IntentFactoryShimModule::class,
      ViewBindingShimModule::class, CachingTestModule::class, RatioInputModule::class,
      PrimeTopicAssetsControllerModule::class, ExpirationMetaDataRetrieverModule::class,
      ApplicationStartupListenerModule::class, LogReportWorkerModule::class,
      WorkManagerConfigurationModule::class, HintsAndSolutionConfigModule::class,
      FirebaseLogUploaderModule::class, FakeOppiaClockModule::class, PracticeTabModule::class,
      DeveloperOptionsStarterModule::class, DeveloperOptionsModule::class,
      ExplorationStorageModule::class, NetworkModule::class, HintsAndSolutionProdModule::class,
      NetworkConnectionUtilDebugModule::class, NetworkConnectionDebugUtilModule::class,
      AssetModule::class, LocaleProdModule::class, ActivityRecreatorTestModule::class,
      NetworkConfigProdModule::class, NumericExpressionInputModule::class,
      AlgebraicExpressionInputModule::class, MathEquationInputModule::class,
      SplitScreenInteractionModule::class,
      LoggingIdentifierModule::class, ApplicationLifecycleModule::class,
      SyncStatusModule::class, MetricLogSchedulerModule::class, TestingBuildFlavorModule::class,
      EventLoggingConfigurationModule::class
    ]
  )
  interface TestApplicationComponent : ApplicationComponent {
    @Component.Builder
    interface Builder : ApplicationComponent.Builder

    fun inject(bottomSheetOptionsMenuTest: BottomSheetOptionsMenuTest)
  }

  class TestApplication : Application(), ActivityComponentFactory, ApplicationInjectorProvider {
    private val component: TestApplicationComponent by lazy {
      DaggerBottomSheetOptionsMenuTest_TestApplicationComponent.builder()
        .setApplication(this)
        .build() as TestApplicationComponent
    }

    fun inject(bottomSheetOptionsMenuTest: BottomSheetOptionsMenuTest) {
      component.inject(bottomSheetOptionsMenuTest)
    }

    override fun createActivityComponent(activity: AppCompatActivity): ActivityComponent {
      return component.getActivityComponentBuilderProvider().get().setActivity(activity).build()
    }

    override fun getApplicationInjector(): ApplicationInjector = component
  }
}
